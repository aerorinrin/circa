# Converter between local and global isotopologue IDs
def local_to_global_iso(mol_id, local_iso):
    """Convert a (mol_id, local_iso) pair to its global HITRAN isotopologue ID."""
    key = (mol_id, local_iso)
    if key not in ISO_DATA:
        raise ValueError(
            f"No global isotopologue ID found for mol_id={mol_id}, local_iso={local_iso}. "
            f"\nSee https://hitran.org/docs/iso-meta/ for the full list of available isotopologues."
        )
    return ISO_DATA[key][0]

